package SignalProc;

public class SampleMissed {

    private int mSampleMissedLoc;

    private int mNoSampleMissed;

    public SampleMissed(int mSampleMissedLoc, int mNoSampleMissed) {
        this.mSampleMissedLoc = mSampleMissedLoc;
        this.mNoSampleMissed = mNoSampleMissed;
    }

    public int getmSampleMissedLoc() {
        return mSampleMissedLoc;
    }

    public void setmSampleMissedLoc(int mSampleMissedLoc) {
        this.mSampleMissedLoc = mSampleMissedLoc;
    }

    public int getmNoSampleMissed() {
        return mNoSampleMissed;
    }

    public void setmNoSampleMissed(int mNoSampleMissed) {
        this.mNoSampleMissed = mNoSampleMissed;
    }
}
